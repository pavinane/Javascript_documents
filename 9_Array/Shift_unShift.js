// Shift is used to remove the first element of the array
let dailyActivities = ["work", "eat", "sleep"];
// dailyActivities.shift();
console.log("remove-item", dailyActivities.shift());
console.log("shift", dailyActivities);

// Unshift is used to add the value in the first of the array
let unshiftActivities = ["work", "eat", "sleep"];
unshiftActivities.unshift("dog");
console.log("unshift", unshiftActivities);
